"""Write a Python program to demonstrate the creation of variables and different data types."""

# Demonstration of variables and different data types

# Integer.
age = 20

# Float.
height = 5.8

# String.
name = "Alice"

# Boolean.
is_student = True

# List.
subjects = ["Math", "Science", "English"]

# Tuple.
coordinates = (10, 20)

# Dictionary.
student = {
    "name": "Alice",
    "age": 20,
    "grade": "A"
}

print("Name:", name, "| Type:", type(name))
print("Age:", age, "| Type:", type(age))
print("Height:", height, "| Type:", type(height))
print("Is Student:", is_student, "| Type:", type(is_student))
print("Subjects:", subjects, "| Type:", type(subjects))
print("Coordinates:", coordinates, "| Type:", type(coordinates))
print("Student Details:", student, "| Type:", type(student))
